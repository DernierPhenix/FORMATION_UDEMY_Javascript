// Opérateurs
// - "+" addition
// - "-" soustraction
// - "*" multiplication
// - "/" division
// - "%" modulo <=> restant de la division ex: 5/2 = 2 Restant 1
// - "**" exponentielle 5**2 = 25

let nombeUn = 4, nombreDeux = 9, result, result2; //déclaration de plusieurs variables sur une seule ligne
result = nombeUn + nombreDeux;

result2 = result * 5
result2 += 2;
result++ //incrémentation - Ajouter 1
result2-- //décrémentation - Retirer 1

console.log(result + " " + result2);